package com.dkl.merchantdb.to;

public class PurchaseOrderTO {

	String itemType;
	String itemName;
	String bond;
	String stockPoint;
	String quantity;
	String weight;
	String weightDiff;
	String pricePer;
	String billPrice;
	String billAmt;
	String unBillAmt;
	String totalAmt;
	String firm;
	
	public String getItemType() {
		return itemType;
	}
	public void setItemType(String itemType) {
		this.itemType = itemType;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getBond() {
		return bond;
	}
	public void setBond(String bond) {
		this.bond = bond;
	}
	public String getStockPoint() {
		return stockPoint;
	}
	public void setStockPoint(String stockPoint) {
		this.stockPoint = stockPoint;
	}
	public String getQuantity() {
		return quantity;
	}
	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}
	public String getWeight() {
		return weight;
	}
	public void setWeight(String weight) {
		this.weight = weight;
	}
	public String getWeightDiff() {
		return weightDiff;
	}
	public void setWeightDiff(String weightDiff) {
		this.weightDiff = weightDiff;
	}
	public String getPricePer() {
		return pricePer;
	}
	public void setPricePer(String pricePer) {
		this.pricePer = pricePer;
	}
	public String getBillPrice() {
		return billPrice;
	}
	public void setBillPrice(String billPrice) {
		this.billPrice = billPrice;
	}
	public String getBillAmt() {
		return billAmt;
	}
	public void setBillAmt(String billAmt) {
		this.billAmt = billAmt;
	}
	public String getUnBillAmt() {
		return unBillAmt;
	}
	public void setUnBillAmt(String unBillAmt) {
		this.unBillAmt = unBillAmt;
	}
	public String getTotalAmt() {
		return totalAmt;
	}
	public void setTotalAmt(String totalAmt) {
		this.totalAmt = totalAmt;
	}
	public String getFirm() {
		return firm;
	}
	public void setFirm(String firm) {
		this.firm = firm;
	}
	
}
